package org.userregistration.regex;


public class PasswordOneupperCase {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a);
        int b = 20;
        System.out.println(b);
        System.out.println(a);
        int temp = a;
        a = b;
        b =temp;
        System.out.println(b);
        System.out.println(a);
    }
}